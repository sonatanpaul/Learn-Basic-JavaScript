const friends = ["Anik", "Rayhan", "Asraful", "Akash "];

// learn for of loop
for (const friend of friends) {
  console.log(friend);
}

// learn for loop
for (let i = 0; i < friends.length; i++) {
  console.log(friends[i]);
}

// learn while loop
let i = 0;
while (i < friends.length) {
  console.log(friends[i]);
  i++;
}

