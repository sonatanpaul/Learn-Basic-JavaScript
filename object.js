// how to declare object
const botle = {
  brand: "Apple",
  color: "White",
  Price: 45,
  isClean: true,
};
console.log(botle);

// how to check object property value
const person = {
  name: "Sonatan",
  age: 25,
  profession: "Web Developer",
  married: true,
};

// dot notation
const personName = person.name;
console.log(personName); // Sonatan

// bracket notation
const personAge = person["age"];
console.log(personAge);

