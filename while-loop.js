// how to check odd  number
let num = 1;

while (num <= 50) {
  console.log(num);
  if (num % 2 == 1) {
    console.log("odd number", num);
  }
  num++;
}

// how to check even number
let even = 1;

while (even <= 50) {
  console.log(even);
  if (even % 2 == 0) {
    console.log("even number", even);
  }
  even++;
}
